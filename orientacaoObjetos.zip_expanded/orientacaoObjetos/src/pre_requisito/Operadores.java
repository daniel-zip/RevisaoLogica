package pre_requisito;

public class Operadores {

	public static void main(String[] args) {
		int n1 = 5;
		int n2 = 10;
		int n3 = 20;
		int resultado;
		
		resultado = n1 + n3;
		System.out.println("Soma de N1 e N3: " + resultado);
		
		resultado = n1 - n3;
		System.out.println("Subtração de N1 e N3: " + resultado);
		
		resultado = n1 * n3;
		System.out.println("Multiplicação de N1 e N3: " + resultado);
		
		resultado = n3 / n1;
		System.out.println("Divisão de N1 e N3: " + resultado);
		
		resultado = n2 / n1;
		System.out.println("Divisão de N2 e N1: " + resultado);
		
	}

}